
    public static void main(String[] args) {
        
        for (int i = 1; i <= 10; i++) {  // Tablas del 1 al 10
            System.out.println("\nTabla del " + i);
            
            for (int j = 1; j <= 10; j++) {  // Multiplicador del 1 al 10
                System.out.println(i + " x " + j + " = " + (i * j));
            }
        }
    }

	
	
	